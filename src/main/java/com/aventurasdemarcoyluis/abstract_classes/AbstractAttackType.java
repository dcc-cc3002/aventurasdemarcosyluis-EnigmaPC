package com.aventurasdemarcoyluis.abstract_classes;

public abstract class AbstractAttackType {
    public int FPCost;
    public double K;

    public AbstractAttackType() {
        this.FPCost = FPCost;
        this.K = K;
    }

    public int getFPCost() {
        return FPCost;
    }

    public double getK() {
        return K;
    }
}
